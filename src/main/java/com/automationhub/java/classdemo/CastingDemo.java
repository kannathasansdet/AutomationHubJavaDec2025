package com.automationhub.java.classdemo;

public class CastingDemo {


    public static void main(String[] args) {

        int a = 10;

        double b = a;

        double c = 10.6;

        int d = (int) c;

        System.out.println("The converted value is " + b );
        System.out.println("The converted value is " + d );

    }
}
